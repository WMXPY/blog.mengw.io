<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <title>Title</title>
</head>
<body>
<div id="app">
    {{name}}
    {{b}}
    {{age}}
    <!--调用获取值-->
</div>
<script src="vue.js"></script>
<script>
    var vm = new Vue({
        data:{
            name:'hello',
            age:'age'
        },
        computed:{
            b:{ //当给b设置属性的时候b立即会调用set方法
                set: function (val) {
                    //在这里对设置的值进行后续的处理
                    //val是设置的具体内容
                    //可以改变原数据的属性
                    //当b更改的时候才触发事件
                    this.name = val;
                    this.age = 500
                },
                get:function () {
                    return this.name + ' world'
                }
            }
        }
    });
    vm.$mount('#app');
    //调用设置值
    setTimeout(function () {
        vm.b = 'zfpx';
    },2000)

</script>
</body>
